  end
  
  if ~diff(e)
    dist = 0;
  elseif inList(e,dist_2500)
    dist = 2.5;
  elseif inList(e,dist_3500)
    dist = 3.5;
  elseif inList(e,dist_5000)
    dist = 5.0;
  elseif inList(e,dist_5590)
    dist = 5.59;
  elseif inList(e,dist_7000)
    dist = 7.0;
  elseif inList(e,dist_7500)
    dist = 7.5;
  elseif inList(e,dist_7900)
    dist = 7.9;
  elseif inList(e,dist_9010)
    dist = 9.01;
  elseif inList(e,dist_10600)
    dist = 10.6;
  else
    dist = [];
    fprintf(['Cannot determine distance between electrodes %d and %d\n',e(1), e(2)]);
  end
  
  
   
